
abstract class WeekDay {}

class Monday extends WeekDay {

  @override
  String toString() {
    return 'monday';
  }
}

class Tuesday extends WeekDay {

  @override
  String toString() {
    return 'tuesday';
  }
}

class Wednesday extends WeekDay {

  @override
  String toString() {
    return 'wednesday';
  }
}

class Thursday extends WeekDay {

  @override
  String toString() {
    return 'thursday';
  }
}

class Friday extends WeekDay {

  @override
  String toString() {
    return 'friday';
  }
}

class Saturday extends WeekDay {

  @override
  String toString() {
    return 'saturday';
  }
}

class Sunday extends WeekDay {

  @override
  String toString() {
    return 'sunday';
  }
}

class Other extends WeekDay {

  @override
  String toString() {
    return 'other';
  }
}

class Unknown extends WeekDay {

  @override
  String toString() {
    return 'unknown';
  }
}